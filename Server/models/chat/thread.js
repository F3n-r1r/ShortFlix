const mongoose = require('mongoose');


/*-------------------------------------------------*\
    3. - THREAD SCHEMA
\*-------------------------------------------------*/
const threadSchema = new mongoose.Schema({
    users: [{
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User'
    }],
    date: {
        type: Date,
        default: Date.now
    }
})


/*-------------------------------------------------*\
    4. - PRE SAVE FUNCTION
\*-------------------------------------------------*/



/*-------------------------------------------------*\
    5. - MODULE EXPORTS
\*-------------------------------------------------*/
module.exports =  mongoose.model('Thread', threadSchema, 'threads');